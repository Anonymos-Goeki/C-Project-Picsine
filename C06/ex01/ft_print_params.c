#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	l;

	i = 0;
	l = 1;
	while (l < argc)
	{
		while (argv[l][i] != '\0')
		{
			write(1, &argv[l][i], 1);
			i++;
		}
		write(1, "\n", 1);
		l++;
		i = 0;
	}
	return (0);
}
